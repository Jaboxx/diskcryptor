static int test_sha512()
{
	sha512_ctx ctx;
	u8        *msg, *key, *dat;
	u8         hash[SHA512_DIGEST_SIZE];	
	int        i;
	
	/* test sha512 hash */
	for (i = 0; i < array_num(sha512_vectors); i++) 
	{
		msg = sha512_vectors[i].msg;

		sha512_init(&ctx);
		sha512_hash(&ctx, msg, strlen(msg));
		sha512_done(&ctx, hash);

		if (memcmp(hash, sha512_vectors[i].hash, sizeof(hash)) != 0) {
			return 0;
		}
	}

	/* test HMAC-SHA-512 */
	for (i = 0; i < array_num(sha512_hmac_vectors); i++) 
	{
		key = sha512_hmac_vectors[i].key;
		dat = sha512_hmac_vectors[i].data;

		sha512_hmac(
			key, strlen(key), dat, strlen(dat), hash);

		if (memcmp(hash, sha512_hmac_vectors[i].hmac, sizeof(hash)) != 0) {
			return 0;
		}
	}

	return 1;
}

