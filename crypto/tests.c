static int test_xts_mode()
{
	u8      key[DISKKEY_SIZE];
	u8      tmp[SECTOR_SIZE];
	dc_key *d_key = NULL;
	int     i, resl = 0;
	u8     *ct, pt[SECTOR_SIZE];
	u64     offset;

	do
	{
#ifdef BOOT_LDR
		d_key = pv(0x5000);
#else
		if ( (d_key = mem_alloc(sizeof(dc_key))) == NULL ) {
			break;
		}
#endif

		for (i = 0; i < SECTOR_SIZE; i++) {
			pt[i] = (u8)i;
		}

		for (i = 0; i < array_num(xts_vectors); i++)
		{
			ct     = xts_vectors[i].ciphertext;			
			offset = p64(xts_vectors[i].index)[0];
			offset = (BE64(offset) - 1) << 9;
			
			autocpy(key + 0, xts_vectors[i].key1, 32);
			autocpy(key + 32, xts_vectors[i].key2, 32);

			dc_cipher_init(d_key, CF_AES, key);

			dc_cipher_encrypt(pt, tmp, SECTOR_SIZE, offset, d_key);

			if (memcmp(tmp, ct, SECTOR_SIZE) != 0) {
				goto exit;
			}

			dc_cipher_decrypt(ct, tmp, SECTOR_SIZE, offset, d_key);

			if (memcmp(tmp, pt, SECTOR_SIZE) != 0) {
				goto exit;
			}
		}
		resl = 1;
	} while (0);
exit:;
#ifndef BOOT_LDR
	if (d_key != NULL) {
		mem_free(d_key);
	}
#endif

	return resl;
}

static int test_buff_crypt()
{
	u8      key[DISKKEY_SIZE];
	u8      pt[SECTOR_SIZE];
	u8      buf[SECTOR_SIZE];	
	dc_key *d_key = NULL;
	u32     e_crc, d_crc;
	int     i, resl = 0;

	do
	{
#ifdef BOOT_LDR
		d_key = pv(0x5000);
#else
		if ( (d_key = mem_alloc(sizeof(dc_key))) == NULL ) {
			break;
		}
#endif

		for (i = 0; i < DISKKEY_SIZE; i++) {
			key[i] = i % DISKKEY_SIZE;
		}
		for (i = 0; i < SECTOR_SIZE; i++) {
			pt[i] = i % SECTOR_SIZE;
		}

		for (i = 0; i < array_num(buff_vectors); i++)
		{
			dc_cipher_init(
				d_key, buff_vectors[i].cipher, key);

			dc_cipher_encrypt(pt, buf, sizeof(buf), 0x1234, d_key);
			e_crc = crc32(buf, sizeof(buf));
			dc_cipher_decrypt(pt, buf, sizeof(buf), 0x1234, d_key);
			d_crc = crc32(buf, sizeof(buf));

			if ( (e_crc != buff_vectors[i].e_crc) || (d_crc != buff_vectors[i].d_crc) ) {
				goto exit;
			}
		}
		resl = 1;
	} while (0);
exit:;
#ifndef BOOT_LDR
	if (d_key != NULL) {
		mem_free(d_key);
	}
#endif

	return resl;
}

